using System.IO;

namespace Microsoft.AspNetCore.HttpLogging
{
    public class Helpers
    {
        public static void DisposeDirectory(string path)
        {
            try
            {
                if (Directory.Exists(path))
                {
                    Directory.Delete(path, true);
                }
            }
            catch
            {
                // ignored
            }
        }
    }
}
